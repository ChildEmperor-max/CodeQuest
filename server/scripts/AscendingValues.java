public class AscendingValues {
    public static void main(String[] args) {
        int x = -25;
        int y = 30;

        int absoluteX = Math.abs(x);
        int absoluteY = Math.abs(y);

        System.out.println("Absolute value of x: " + absoluteX);
        System.out.println("Absolute value of y: " + absoluteY);
    }
}