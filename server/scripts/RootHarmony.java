public class RootHarmony {
    public static void main(String[] args) {
        double x = 64.0;
        double y = 100.0;

        double rootX = Math.sqrt(x);
        double rootY = Math.sqrt(y);

        System.out.println("Square root of x: " + rootX);
        System.out.println("Square root of y: " + rootY);
    }
}